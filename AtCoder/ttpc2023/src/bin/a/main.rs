use proconio::input;

fn main() {
    input! {
        n: usize,
    };

    let mut ans = 0;
    let r#mod = 998244353;
    for i in 2..=n {
        ans = ans * 2 + (i - 1);
        ans %= r#mod;
    }
    println!("{}", ans);

}

